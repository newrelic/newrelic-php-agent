function c()
{
    time_nanosleep(0, 100000000);
    echo fraction(5) . "\n";
    echo fraction(0) . "\n";
}

function b()
{
    time_nanosleep(0, 100000000);
    try {
        c();
    } catch (RuntimeException $e) {
        echo("Caught exception: " . $e->getMessage() . "\n");
    }
    newrelic_notice_error(new Exception('Sample Exception b'));
}

function fraction($x) {
    time_nanosleep(0, 100000000);
    if (!$x) {
	return 0;
    }
    return 1/$x;
}

function a()
{
    time_nanosleep(0, 100000000);
    echo "Hello\n";
    newrelic_notice_error(new Exception('Sample Exception a'));
};

a();
b();
